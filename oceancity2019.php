	<div class="copy e-content">
		<p>We decided to take a mini vacation and celebrate <data class="p-category" value="localssummer">#localssummers</data> for three days September 5th to 7th. The warm weather and lack of crowds makes September an ideal time to vacation. The trip also marked a bitter sweet milestone as we would stay in my parents new nhouse since the family compound in Black Point, CT got sold after 50 years.</p>
		<p>With Dorian racing, more a slow crawl, up the Eatern Seaboard we also did not know if the trip would happen. Luckly the weather held out and we got two amazing days with family.</p>
