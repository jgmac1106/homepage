 <p class="p-name e-content">
     My writing <br>
a broken string <br>
      on an out of tuned guitar<br>
That, word, the chord, that perfect note<br>
   missing<br>
      my headstock<br>
a few nuts short<br>
Yet I strum on, noises lost<br>
   to an empty audience who <br>
	     spill from my pages<br>
	on to streets so sweet<br>
	   paved in my desires<br>
	 sinking into the soundhole<br>
	 of my soul.
