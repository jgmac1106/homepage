   Torchlight<br>
&nbsp;&nbsp;saddles<br>
&nbsp;&nbsp;&nbsp;&nbsp;strike flint<br>
sauntering skyward<br>
 &nbsp;golden<br>
 ore, veins of light<br>
 on painted shores<br>
keeping time at cliff's edge<br>
 Weightless girth<br>
 &nbsp;&nbsp;Clinched to<br>
 unfettered<br>
 &nbsp;&nbsp;&nbsp;&nbsp;minds<br>
 Brow bent<br>
 &nbsp;over pommel<br>
 as parallel pasts<br>
 &nbsp;&nbsp;stride in <br>
 &nbsp;&nbsp;shards of<br>
 aspects glaring<br>
 down on cognizance of<br>
  &nbsp;&nbsp; spinning consciousness<br>
  in a dominance<br>
  of stirrups<br>
  boot worn by 
  &nbsp;the search. A<br>
  &nbsp;&nbsp;&nbsp;Need<br>
  For the Zen<br>
  of Provenance<br> 
