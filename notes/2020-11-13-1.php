 <p class="p-name e-content">
Night falls early now<br>
And I'm tired.<br>
Just shy of five.<br>
And so so tired<br>
I lean against a rock<br>
put my beer against a dying tree<br>
and watches as night rises<br>
Over a future, I've never believed in<br>
