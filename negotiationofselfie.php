<p>Two giants<br/>
bending realities in<br/>
Unified dimensions<br/>
of altering axis<br/>
0f self, A<br/>
negotiation shifted<br/>
in identities defined for us<br/>
as we cast I , me, you we,<br/>
into the heavens<br/>
gravitational waves of reality<br/>
as we can't see the physics within<br/>
the same<br/>
identities,<br/>
projections which<br/>
reflect<br/>
Me(s)<br/>
Within we(s)<br/>
If literacies, multiple<br/>
identities must follow<br/>
<br/>
You, me, I, we<br/>
project quantum<br/>
fluctuations on thee<br/>
<br/>
society,<br/>
a projection of<br/>
relativity<br/>
Like Einsten's<br/>
papers, two realities<br/>
negotiate an<br/>
existence where<br/>
neither can truly be<br/>
<br/>
Accelerating truths<br/>
bring on new<br/>
weights of gravity<br/>
time dilations of frozen crisis,<br/>
a maligned star<br/>
burning with the fusion of a<br/>
broken heart<br/>
<br/>
and expeditious explosion<br/>
of joy as diamond<br/>
caviar<br/>
cascades from neutrino<br/>
stars.<br/>
<br/>
An Agent of action<br/>
sending ripples<br/>
through time and space<br/>
While<br/>
Quantum mechanics<br/>
on a cosmic scale<br/>
dance in minutiae of self<br/>
through<br/>
four dimensions<br/>
of space spinning in<br/>
gyrations of souls<br/>
unified systems in unpredictable<br/>
energies<br/>
<br/>
heavenly bodies, us each,<br/>
big objects, planets,<br/>
colliding galaxies<br/>
our Me(s) warped around us by<br/>
Newton's laws<br/>
Identity, like<br/>
Gravity birthed int he<br/>
Enlightment<br/>
Yet that same<br/>
magnetism of will,<br/>
draws force from the well<br/>
that<br/>
compels love<br/>
both strong and weak<br/>
is it a fourth power we seek?<br/>
for<br/>
“Only the hand that erases<br/>
can write the true thing.”<br/>
Each quarky twitch<br/>
'a switch behind two<br/>
indescribable forces<br/>
writing your yous<br/>
onto the world<br/></p>
