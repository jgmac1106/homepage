  <p>When Jess suggested we start up a The Lemonade Club 5k remote challenge I really liked the idea. I have not run a 5k since my father's 60th birthday party 5k....almost 14 years ago. Plus it sounded like a great way to stay social while we physically distance.</p>
  <p>Yesterday I, and the boys, started training. Last year I completed a trail in the woods behind the house. Each lap takes a runner ~2,510 feet (I need to manually measure GPS a bit flaky). In 23 minutes Danny (my youngest at 7) ran three laps. Ben (9) clocked in one lap and John (11) ran one lap after we finished. Having Danny as a pacer helped. I slowed down "for him" I "totally could have kept running without the winding"...okay sure</p>
  <ul>
    <li>Feet Ran: 7530</li>
    <li>Elapsed Time: 23 minutes</li>
    <li>Miles Ran: 1.43</li>
    <li>KM Ran: 2.30</li>
    <li>Time: 23 minutes</li>
    <li>Elevation (uphill) 265 ft</li>
    <li>Elevation downhill -319 ft</li>
  </ul>
  <p>So it looks like we got about half way there our first attempt. Another 1.67 miles. Not too bad out of the gate. 8,817 would take 3 and 1/2 more laps. Now it sounds more daunting once I do the math. Still I think we can get there by the time the remote 5k gets scheduled.</p>
