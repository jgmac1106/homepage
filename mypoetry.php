  <h3 class="p-name">Untitled Poem</h3>
  <p class="p-summary">A collaborative poem written <br/>with Kevin Hogdson <br />every day for 30 Days</p>
  <div class="e-content">
    <p class="Greg">
      Skin plastered against tattered holes<br />
of a torn blanket.<br>
Rough threads of <br>
unwoven time<br>
scratching against frozen thighs<br>
She sighs, glancing beyond<br>
a shoulder tucked close to<br>
heart<br>
beyond the cold<br>
frosted window she see nothing<br>
nothing at all<br>
</p>
<p class="Kevin">
 Fingers caress<br>
these holes, torn,<br>
of this blanket, worn;<br>
She threads time<br>
like a shoulder tucked<br>
close to the heart,<br>
the start of nothing<br>
at all<br>
</p>
       <p class="Greg">
      The moon collapsed over horizons split<br>
shattered space in<br>
illuminating mysterious truths<br>
as time melts<br>
answers hang above broken canopies<br>
towering conceptions move as the rabbit<br>
dances<br>
feet wrapped in dew fed grass<br>
energy from within<br>
fleeting realities in a world<br>
undefined<br>
<br>
