   <p>I recently published a poem <a href="https://stream.jgregorymcverry.com/2022/03/19/smallpoemsstuck-in-my.html">Redshift"</a> and someone pinged me and asked how I wrote the poem. The poem tries to capture the feeling of weight by using everything from Newton's First Law (Inertial Frame) to gamma ray burst as a metaphor for every day life.</p>
<p>First I often write about space and gravity. When you talk about forces billions of years old at a scale of energy we can never imagine you have good material that almost approaches the mysteries of the human condition.</p>
<p>I am also lazy. Coming up with new ideas is hard. Many of the poems I write each day map perfectly to the featured article or "on this day" feature of Wikipedia. I love this feature I could chase rabbit holes forever. Sometimes these journeys become poems.</p>
<p>I read the following articles for the poem:</p>
<ul>
   <li><a href="https://en.wikipedia.org/wiki/Main_Page">Wikipedia Homepage</a></li>
   <li><a href="https://en.wikipedia.org/wiki/Redshift">Redshift</a></li>
      <li><a href="https://en.wikipedia.org/wiki/Gravitational_redshift">Gravitational Redshift</a></li>
<li><a href="https://en.wikipedia.org/wiki/Lorentz_covariance">Lorentz Covariance</a></li>
<li><a href="https://en.wikipedia.org/wiki/Inertial_frame">Inertial Frame of Reference</a></li>
<li><a href="https://en.wikipedia.org/wiki/Triangulum_Galaxy">Triangulum Galaxy</a></li>
<li><a href="https://en.wikipedia.org/wiki/Local_Group">Local Group</a></li>
<li><a href="https://en.wikipedia.org/wiki/Milky_Way">Milky Way</a></li>
<li><a href="https://en.wikipedia.org/wiki/Phoenix_Dwarf">Phoenix Dwarf</a></li>
<li><a href="https://en.wikipedia.org/wiki/Superluminous_supernova">Superluminous Supernova</a></li>
<li><a href="https://en.wikipedia.org/wiki/Supernova#Core_collapse">Core Collapse</a></li>
<li><a href="https://en.wikipedia.org/wiki/Electron_capture">Electron Capture</a></li>
   </ul>
   <p>As I would read I would then take notes to form the poem. In the first phase I just collect phrases I think sound cool. I look for phrases that easily have double meanings or where I can allude to everyday struggles utilizing an antithesis of paradigms.</p>
   <img width="60%" src="https://jgregorymcverry.com/photos/redshiftphoto1.jpg" alt="screenshot of journal">
   <p>I then draft the poem. Usually in my journal. You also see all the names for the Triangulum Constellation. I thought that would make a good poem too. Babylonians called it "The Plow." It marked the start of Autumn harvest. The Chinese call the constellation "Heaven's Great General." Poem pretty much writes itself.</p>
    <img width="60%" src="https://jgregorymcverry.com/photos/redshiftphoto2.jpg" alt="screenshot of journal">
    <p>I would never recommend poetry as a way to synthesize information about science (I would still make it a playlist assignment choice) but it really works for me. When you transform phenomenon from billions of years ago in to arranged phonemes the information you use remains sticky.</p>
    <p>In the end I have no secret sauce to writing poetry. I read multiple sources, take notes, draft an idea, and publish a poem. Like gravity. The Writing Process is a constant that can not change without altering your frame of reference.</p>
