<p>Modes of Instruction and Learning</p>
</td>
</tr>
<tr>
<td>
<p>This class is organized around five units. Every unit is composed of related topics. Each topic is broken into a read, write, and participate task. Everything will be hosted in Blackboard. You may choose to use Blackboard or your own website. An RSS feed will be provided but all students must upload links or submit assignments to each Module in Blackboard.</p>
<p>Read Task</p>
<p>We will utilize <a href="https://hypothes.is&amp;sa=D&amp;ust=1590423280631000">https://hypothes.is</a>&nbsp;to annotate and take notes on one reading per topic. You are welcome to utilize the social annotations tool on the other readings as well.</p>
<p>You need to track and share how you take notes as you read. This may be a blog reflection or a screenshot of your notes. The goal is to model the types note taking strategies you will need to teach students in grades 3-6</p>
<p>Each topic will have an assigned discussion director, a word hunter, and a digging deeper detective. You will have all three roles at different times during the class:</p>
<ul>
<li>Discussion Director: You will post questions about the readings to your group. All questions get approved by Dr. Mac before they are posted. You then facilitate the discussion of that topic during the week.</li>
<li>Word Hunter-You have to identify all key words or concepts from the readings or topics and add to the class wiki.</li>
<li>Digging Deeper Detective</li>
</ul>
<p>Write Task</p>
<p>You then take the information you learned from the readings and synthesize this into your own original writing. These are concise statements or deep questions. The key factor in success is utilizing the evidence from the readings and your peers and combining this with your experience or what you know.</p>
<p>Strategies:</p>
<p>Pre-plan: Paper is your friend. Research even shows it improves learning. Draft: Write and revise</p>
<p>Features of a successful blog post</p>
<ul>
<li>A well organized argument or main point, supported through the use of hyperlinks and multimedia materials (video, sound, images)</li>
<li>A clear, unified voice that is appropriate for the intended reading public</li>
<li>A title that links clearly to the content of the post</li>
</ul>
<p>Features of a successful blog</p>
<ul>
<li>Short, concise posts that use multimedia materials (video, images) to effectively make a point</li>
<li>Effective use of hyperlinks to support ideas, to direct readers to relevant, interesting posts, and to demonstrate an awareness of the community of writers focusing on a common theme or set of ideas</li>
<li>A clear, unified voice that continues to grow and develop over time</li>
</ul>
<p>Qualities of a skilled blogger</p>
<ul>
<li>Ability to quickly synthesize and articulate ideas</li>
<li>An awareness of a wide range of blogging techniques and of how these various techniques reach different target audiences effectively or ineffectively</li>
<li><a href="https://jennamcwilliams.blogspot.com/2009/04/writing-with-mouse-in-hand.html&amp;sa=D&amp;ust=1590423280648000">Reading with mouse in hand</a>: Engaging with (online and offline) materials as potential material for blog posts</li>
<li>Willingness to serve as an intelligent filter for a wide public audience</li>
<li>Engagement with the wider blogging community, including offering thoughtful comments on others&rsquo; writing and reading widely and broadly</li>
</ul>
<ul>
<li>Post by Jacob McWilliams.</li>
</ul>
<p>Participate Task</p>
<p>Every topic has a participation task that is designed to let you demonstrate mastery of the stated objectives. Each module page in Blackboard will describe the criteria and different technology requires (like Google Docs, Google Slides, etc).</p>
<p>Performance Assessment</p>
<p>Each unit ends with a performance assessment. You will submit your plans and reflections on how you complete these tools that measure your growth in skills and knowledge.</p>
