<p>Grading Policy</p>
</td>
</tr>
<tr>
<td>
<p>This is an intensive five week class. Success is determined by your commitment to learning rather than relying on rubrics and numbers. You need to demonstrate the knowledge, skills, and dispositions to be a successful classroom teacher</p>
<p>Individual Tasks</p>
<p>You are required to post all read and write post by the stated dates in the syllabi. This provides evidence of your ability to set and accomplish goals. These assignments are scored dichotomously. You did or did not.</p>
<p>However patterns of evidence will emerge across these posts that should show growth over time.</p>
<p>Performance Task:</p>
<p>All performance tasks are judged against the stated criterion. You should ensure your artifacts demonstrate mastery of the goals. After each module you are to post a reflection explaining what you did, how you did it, and how this met the criteria.</p>
<p>If I feel you did provide evidence of this growth I will award you a credential. Successful performance i class requires you to collect all the badges.</p>
<p>Group Task:</p>
<p>Your dispositions as a teacher will be assessed during your group task. How well do you communicate, plan, and write with others is a key skill for teachers,</p>
<p>A survey will be sent to try and match members with availability and preferred methods of communication.</p>
<p>Alternative assignments will not be provided for those who do not complete scheduled group tasks. If a group member is not performing their duties email Dr. Mac.</p>
