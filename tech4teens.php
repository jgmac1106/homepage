    <p>Concepts for Adaptive Tehnology, Bella Xiong, a Yale Computer Science student, and myself are planning a free technology camp this summer for Connecticut Teens. Students will have mentors from Yale Computer Science and students in Southern Connecticut State Education educator and librarian programs.</p>
    <p>We can scale this poject across the state, and technically nationally quite easily. Kids needs camp. Teacher prep programs need digital field placements. We can make it happen.</p>
    <p>More importantly we should now, more than ever, root the web in places where #literacies belong: schools and libraries. Algorithims should not determine adolescents.</p>
    <p>If you would like to support our  Tech4Teens program please contact <a href="mailto:jricker@cfalct.org">Jennifer Ricker</a>, Executive Director of CfAL. We always need more mentors and money. If you want to learn more about fulfilling field placement requirements reach out to me.</p>

