  <h1 class="p-name">Striving for Remote Parity: Reducing the Carbon Crater Left By Academic Conferences</h1>
  <div class="pubinfo">
  <p>Published by <a class="p-author h-card" href="https://chendricks.org/">Christina Hendricks</a>, <a class="p-author h-card" href="https://autummcaines.com/">Autumm Caines</a>, and  <a class="p-author h-card" href="https://jgregorymcverry.com">J. Gregory McVerry</a>
