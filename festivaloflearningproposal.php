    <p class="p-summary">How Can We Reduce the Carbon Impact of Academic Conferences?</p>
    </span>
 </div>
 <img class="featured u-featured" src="/photos/conferenceclimate.png">
    <div class="copy">
  <div class="e-content">
    
<p>As our world burns, many scholars around the world seek to lower their impact on the climate crisis. One of our biggest carbon sinks fills as we seek out conferences. Thousands of academics with the privilege and power to travel descend on cities through air, rail, and car, with each conference trip &ldquo;amounting to 7% of an average individuals annual C02 emissions&rdquo; according to one study (Spinellis and Louridas, 2013). Sales reps pitch consumption in vendor halls awash in wasteful swag. Is there a better way forward?</p>
<p>This session examines case studies of alternative conference models that can drastically reduce the carbon impact of conferences while also embracing open pedagogies and providing greater access to those for whom long-distance travel to conferences is difficult or impossible. The session will itself model a hybrid approach to conference sessions, with some facilitators and participants attending onsite and some online.</p>
<p>We will begin with a brief introduction of the impact academic conferences have on our carbon footprint. Then the facilitators will present case studies of alternative conference models, such as <a href="https://www.google.com/url?q=https://pressedconf.org/&amp;sa=D&amp;ust=1575117230490000">PressEd conference </a>(takes place entirely on Twitter), <a href="https://www.google.com/url?q=https://indieweb.org/&amp;sa=D&amp;ust=1575117230490000">IndieWebCamp</a>&nbsp;(a global movement of local events), and <a href="https://www.google.com/url?q=http://virtuallyconnecting.org/&amp;sa=D&amp;ust=1575117230490000">Virtually Connecting</a>&nbsp;(a community that connects people at conferences with those not onsite).</p>
<p>After each facilitator presents, small groups onsite and online (through breakout room in a videoconference) will discuss the benefits and drawbacks of traditional and alternative conference models around topics such as:</p>
<ul>
<li>Equity, diversity, inclusion, accessibility</li>
<li>Learning from people outside one&rsquo;s local network</li>
<li>Open practices</li>
<li>Costs (in terms of money and time/effort; both to host and participate)</li>
</ul>
<p>We will then ask participants to reflect on and discuss in their small groups concrete suggestions they can give to people who organize specific conferences they are involved in (so participants will come away with a specific action item). At the end of the session we will reconvene as a large group and ask a few people to share insights and suggestions from their small group discussions.</p>
