   who shower gifts on those willing to wash<br />
 <a href="h-cite" href="https://quickthoughts.jgregorymcverry.com/2020/02/29/a-priest-knockingon-rantidevs-doorso-differentthan-please">feet</a> of guests</p>
 <p>Poetry is welcoming new risk<br />
giving our last words to <a span class="h-cite" href="https://quickthoughts.jgregorymcverry.com/2020/02/29/warming-kneesby-the-fire-in-tales-ofhavamal">strangers</a><br >
at risk of no thanks<br />
forget about pay<br />
<p>Poetry is welcoming words<br />
  poured endlessly from Herme's pitcher<br />
to slay giants, <a class="h-cite" href="https://quickthoughts.jgregorymcverry.com/2020/02/29/good-to-remember-greatest-of-heroesfall-in">history's horsefly</a><br />
whose bite brings sounds to 
flight as pixies dance in waterfalls<br />
singing blessings of the <a class="h-cite" href="https://quickthoughts.jgregorymcverry.com/2020/02/29/like-the-blessings-of-bakhty-hangiven-to">birch tree</a><br />
we ride our <a class="h-cite" href="https://quickthoughts.jgregorymcverry.com/2020/02/29/trickster-rabbitof-the-eastern-shorethere-you-gobumbling">miseries on glories</a> stolen<br />
mounted pride, that must subside<br />
to steal glances of the other side<br/>
</p>
<p>Poetry is <a class="h-cite" href="https://quickthoughts.jgregorymcverry.com/2020/02/29/sipping-on-teawith-sen-no-rikyueach-sipwelcoming">welcoming strangers</a><br />
