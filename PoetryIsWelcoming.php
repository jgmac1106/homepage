     <p>Poetry is welcoming<br />
    A magic arrow chasing away <a class="h-cite" href="https://quickthoughts.jgregorymcverry.com/2020/02/29/a-magic-arrowcasts-asides-asangry-bearssquatting-homes">greedy bears</a><br />
    who sleep amongst us<br />
    while a <a class="h-cite" href="https://quickthoughts.jgregorymcverry.com/2020/02/29/maybe-the-moraldoesnt-lie-with-themouse-and">raven pulls on a rat</a><br />
    carrying truths of harsh reality<br />
    Poetry will <a class="h-cite" href="https://quickthoughts.jgregorymcverry.com/2020/02/29/whether-draping-the-couchesor-telmachus-sparing-goddesses">drape the couches</a><br />
    covering cloth of lies with veneers<br />
    of plastic truths<br />
  </p>
  <p>Poetry is welcoming new Truths<br />
    Naming <a class="h-cite" href="https://quickthoughts.jgregorymcverry.com/2020/02/29/some-sad-truths-toldin-genesisyet-lot-so">Lot's Wife</a><br />
    not of a pillar of sin<br />
    but a strong woman who wouldn't<br />
    give in</p>
    <p>Poetry is welcoming <a class="h-cite" href="https://quickthoughts.jgregorymcverry.com/2020/02/29/a-lumber-jacksaxe-rest-upon-a-treeas">shade</a><br/>
    under trees to topple<br/>
    our axe grinding way<br />
    realities that can not fade<br />
    like the loyalty of a pup<br />
    <a class="h-cite" href="https://quickthoughts.jgregorymcverry.com/2020/02/29/lo-sunlosing-fanhero-or-rivertrusted-friendwelcoming-sightsaving-a">saving signt and men</a> from sea<br />
