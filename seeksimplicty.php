<p>Seek simplicity<br />
of seasons in all you do.<br />
A child's smile in<br />
the mundane. As Monday melts<br />
to Tuesday, a sublimation of<br />
time<br />
Art found in patterns<br />
of your sponge<br />
gliding over dishes<br />
wishes undone by<br />
clear direction of a risen<br />
sun<br />
not tomorrow, not yesterday<br />
Just Now<br />
A solstice of small<br />
moments stolen<br />
from an equinox<br />
of complexity that<br />
recedes in slow puffs of the<br />
Soul<br />
#smallopoems</p>
