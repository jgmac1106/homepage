    <h3>Privacy is not a Default. It is a Priviledge</h3>
    <p>The results of this study demonstarte that students who may want the most privacy in #OpenPedagogy may be the most ill equipt with the technological skills to ensure they can control their data.</p>
    <p>This was most evident in the comments (not cited for pricvacy reasons) in student feedback. Students wanted the safety of the LMS no matter how I explained the benifits of open</p>
    <p>Privacy si also hard. After noting this pattern I went back to watch video tutorials on open pedagogy and data privacy. Few of the platforms make it easy. The new WordPress changes for example made it hard for students to determine how to make post private.</p>
    <h3>Retreating Back to the LMS</h3>
    <p>Based on the needs and desires of the students, as that matters more than my commitmment to #OER and #OpenPedagogy, I retreated back to the LMS and am not teaching solely through RSS and chat.</p>
    <p>My teacher prep classes use Blackboard and students have the option to choose their own blogs. Only a small percentage chose not to use Blackboard</p>
    <p>For my edtech classes I went back to the <a href="https://withknown.com">Known CMS</a>.This platform gets privacy as a default correct. Students can publish publicly, members only, or private just to me.</p>
    <h3>Cognitive Load Makes Students Feel Unsafe</h3>
    <p>Many of my students reports worrying about trying to figure out all the content, how to set up a website. A few worried about having to make sure their work was perfect because it was in the public (positive or negative?)</p>
    <p>Many students shut down in the face of navigating all the great tools that support #OpenPedagogy. They wanted one place to rule them all.</p>
    <h3>Social Practices Around Grading Cause Anxiety</h3>
    <p>My Code of Conduct and syllabi need to do a better job of explicitly spelling out my ungrading policy. Many students, even though they got comments on all their writing, and badges when they met criteria felt I did not grade enough and they never knew how they were doing in the class.</p>
