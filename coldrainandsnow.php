     <p>On May 8-9th 2020 a polar vortex decsended over much of North America. Tempatures plummeted, a cold ripping rain moved in followed by temperatures that found no bottom. Our friends in Warren, CT woke to a half inch of snow. Our Saturday filled with only flurries and a furiou wind.</p>
     <p>Weather would be weird on a normal day. Forty three years since last accumulating snow in May. Combined with COVID-19 many found the cold too much. Sense of impending doom just set in deeper as the weather chilled your bones.</p>
     <p>A week ago the sun brought hope. Many took the kiss of warmth, the first three day stretch above sixty degrees since October has a sign of hope. Then the cold came. Covid-19 still here. School closed for the year.</p>p>
     <p>Mother's day looking up and all can not be lost when we can turn to poetry. Below are my three poems from the Covid-19 Polar Vortex</p>
     <h2>Not Day</h2>
     <audio class="u-audio" src="https://jgregorymcverry.com/poetry/notday.mp3" controls="controls">
</audio>
<p>This poem brewed for a bit in my head. I wanted to play with a concept of days not being any different and indistinguishable. I walked around a bit dragging that long a sound around with me. Then after finishing up for the work the day I headed outside</p>
<p>The rain has already begun and I had worked out much of the poem to the point I knew if I did not write it down I would lose it forever. Problem, though I already geared up. Rain coat and pants, muddy shoes. I could not reneter the home.</p>
<p>So I grabbed an old newspaper, an unsharpened pencil and pocket knife and trudged off into the words. The poem details my inability to actually sit down and write the poem. In the end the weirdness of the day made it one to remember kind of negating the original poems intent.
  <h2>May's Winter Wind</h2>
<audio class="u-audio" src="https://jgregorymcverry.com/poetry/MaysWinterWind.mp3" controls="controls">
</audio>
<p>Not sure if the pollen tickled my nose, my eyes did burn, but that frigid wind carried something I wanted to capture. I sat outside in the yard. During a brief respit of sun, clouds breaking overhead, leaning against the truck and the scent hit me. I smelt a little minter in May. Given how depressing the weather fwlt this coll fact had to be a poem.
</p>
<h2>Spring Silence</h2>
<audio class="u-audio" src="https://jgregorymcverry.com/poetry/SpringsSilence.mp3" controls="controls">
</audio>
<p>I miss the mark a bit on this last one. Trying to hard maybe to find the meaning in the mundane. I love walking the wood in rain. You see truths revealed in ways not possible. The way rain falls off bent branches marking an animal path invisble to world before the steady drips. Cold rain also brings a warming blanket of solitude not possible. Everything quiet. Everything gone. But the rain. Truth in that somewhere, I'll keep looking. </p>
