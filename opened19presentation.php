        <p class="p-name e-content">#OpenEd19 As a theoretical lens for our self study I drew upon Dewey's notions of democratic education. As educators we must shape the spaces of learning to meet ideals of democratic education (Dewey 1934). For Dewey democracy is a way of being, of experiencing the best collective action humans  could muster. The web is no different. Building a better web, through OER, builds a better democracy.
        </p>
        
