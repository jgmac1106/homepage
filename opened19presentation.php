        <p class="p-name e-content">#OpenEd19 Dewey (1927) noted, ‘[a] Great Community can only occur with free and full intercommunication’ (p. 211). In terms of ensuring an open AND private classroom  we must build a shared experience around common goals with elements of experimentation and criticality (Bruce & Bishop, 2005). Such community inquiry allows people to construct knowledge from both the personal and the collective (Shore et al., 1996).
