  <h1 class="p-name">Growing the  #IndieWeb at IndieWebCamp New Haven</h1>
  <div class="pubinfo">
  <p>Published by <a class="p-author h-card" href="https://jgregorymcverry.com">J. Gregory McVerry</a>
     on <time class="dt-published" datetime="2019-04-05 08:38:00">5<sup>th</sup>April 2019</time></p>
  <p class="summary p-summary">
    Wrap up overview of IndieWebCamp New Haven
    </p>
 </div>
    <img class="featured u-featured" src="https://quickthoughts.jgregorymcverry.com/file/607fc915723937cd53befbaaa908a7c2/thumb.jpg">
    <div class="copy">
  <div class="e-content">
  <p>On March 30-31st we held the first IndieWebCamp in New Haven. Throughout the weekend six people joined us in person and we had five people join us online. Most importantly we welcomed two people online who had never built a website before.</p>
<h2>
  The Sessions
    </h2>
    <h3>
      <a href="https://indieweb.org/2019/New_Haven/onboarding">Onboarding</a>
    </h3>
    <p>
      In this session we went through strategies to improve onboarding for people new to #IndieWeb. Using a six word memoir and storyboarding we modeled how to write yourself on the web 
    </p>
    <h3>
      <a href="https://indieweb.org/2019/New_Haven/books">Books</a>
    </h3>
      <p>
        How can we publish from our own domain? What tools are out there?
      </p>
      <h3>
        <a href="https://indieweb.org/2019/New_Haven/gradebook">Critical Gradebook</a>
      </h3>
     
        <p>
        How do we assess the #IndieWeb classroom? What tools are there now and what could the future look like?
      </p>
    <h3>
      <a href="https://indieweb.org/2019/New_Haven/annotation">Owning Your Annotations</a>
    </h3>
    <p>
      Open annotation and the #IndieWeb. Strategies to annotate from your own Domain.
    </p>
    <h2>
      Hack Day
    </h2>
    <img src="https://quickthoughts.jgregorymcverry.com/file/d73c2f8ace7fc438c6768118f9e7055c/thumb.jpg">
    <p>
      On hack day we got right to work. David Shanske continued his installation of Compass and hooking that into <a href="https://david.shanske.com/2019/03/31/setting-up-compass-with-gps-logger-for-android/">GPSLogger</a> and then releasing it on <a href="https://david.shanske.com/2019/03/31/simple-location-3-6-3-released">Simple Locations</a> plug in for Wordpress
    </p>
    <p>
      <a href="http://francettecarson.com/">Francette Carson</a> released her first website. She built it using WordPress and the SemPres theme. Collette needs to develop a web presence in order to publish her novel. 
    </p>
    <p>
      <a href="http://ladydysevents.com/">Lady Dy </a>built her first website also using SemPress. She needs to create an ecommerce site for her "Party in a Box" business. Basically she sends out all the decorations and materials for a kids birthday party so parents do not have to think about.
    </p>
    <p>
      <a href="http://drewmcweeney.com/">Drew McWeeney</a> worked on his <a href="https://consulting.drewmcweeney.com/">consulting page</a>for lifegaurding training he offers as part of the Red Cross. Drew spent some time learning about CSS and hover effects.
    </p>
    <h2>
      Having Fun
    </h2>
    <p>
       We set up a variety of social activities. Thanks to our sponsors we tried some of the local diners, and a Halal fusion restaurant. Then for the meet up we did a little Disco Duck Pin Bowling and ate at DiMatteo's (best pies in Hamden...but I got the linguini and clams).
    </p>
