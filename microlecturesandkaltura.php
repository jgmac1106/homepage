<p>Goal: Create and upload a microlecture using the screencapture software Kaltura.</p>
<p>Objectives:</p>
<ul>
<li>Identify best practices in microlectures</li>
<li>Storyboard a video</li>
<li>Create and record a video with a screenshare</li>
<li>Publish and embed video in a Blackboard course</li>
</ul>
<p>Schedule:</p>
<ul>
<li>5 minute entrance slip</li>
<li>10 minute analyze a video</li>
<li>5 minutes effective practices</li>
<li>10 miunte Kaltura tour</li>
<li>20 minutes Plan a video</li>
<li>10 minutes record and upload</li>
</ul>
<p>What does the research say?</p>
<p>Shorter videos matter. In studies of online videos students will stop watching after about ten minutes, with seven minutes being the sweet spot for engagement (Guo, 2014). We also know from multimedia learning theory that people learn better with pictures and words alone (Mayer, 2008). Even with shorter videos you need to hold students accountable for watching videos (Darby &amp; Lang, 2019). Finally videos where proffesors actively preplanned are more effective learning tools (Guo, 2014).</p>
