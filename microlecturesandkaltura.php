<p>Analyze Exemplar Text</p>
<p>Have participants choose one of the four videos identify common elements. You want the audience to see that you activate prior knowledge, state the objectives, explicitly define key terms, model what you want students to know and do, keep them short.</p>
<p>Explain that a good microlecture is much like a lesson plan</p>
