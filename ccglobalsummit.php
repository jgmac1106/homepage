    <h2 class>
      Abstract
    </h2>
  
    <p class="p-summary">Join us on a story of a Commons of Knowledge unfolding, reinforcing and growing as we describe how the goals, practices, and tools used by the IndieWeb community interact as symbiotic building blocks.  </p>
    <h2>
      Diversity
    </h2>
    While both presenters originate from a place of privledge in the global north we will focus much of the conversation by discussing historical examples of "open"  being a multiplier effect of privledge (<a href="https://hapgood.us/2011/12/09/openness-as-a-privilege-multiplier/">Caulfield, 2011</a>). While recognizing there is still much work to be done, we will also highlight recent efforts the IndieWeb community has made to intentionally improve diversity and inclusion. We will also present remixes of under represented and marginalized peoples empowered by owning their content on the IndieWeb. Finally we will welcome any voice from around the world to join our session. 
    <h2>
      Outcomes
    </h2>
    <p>
    Participants will indentify socio-technical solutions to helping the CC Global Networks. We then develop strategies for supporting open learning in the CC Glbal Network by encouraging the use of personal websites as tools for reflective growth.
    </p>
    <h2>
      Full Description
    </h2>
    <h3>
      Growing Our Networks by Remixing other Models
    </h3>
    <p>As the Creative Commons community seeks to grow we must remember the Commons is so much more than two people. It's more than some transaction of knowledge, regardless of the amounts of labor and love. We aren't just sharing a resource between a creator and learner. The Community is the curriculum and the Movement is the space, the  people and the knowledge. In order to best understand how we can stand on a stool built of these three legs we should examine the history of allies who created sustaining models of growth.</p>
    <p>
      The IndieWeb community, a group of people committed to an alternative the corporate web, provides a case study for the  CC Global Network. IndieWeb which organizes around shared values of owning your content, enhancing your connections, and controlling your space. These values are then lived through a set of principles as individuals live on the web from their own websites.
    </p>
    <p>
      TLDR: The world works better when we all have our own website and even better when you live on your own domain.
    </p>
    <h3>
      Session Organization
    </h3>
    <p>
      In this session, the presenters, both organizers in the community will share stories, by remixing blog posts selected from  over 2300 websites, close to six million webpages and about 400 gigs of HTMl data. These posts and artifacts will be organized around five themes:
    </p>
    <ul>
      <li>Building your own blog: Any participant attending the session can leave with their website and ready to spread joy in thier CC Global Networks. </li>
      <li>Organizing Community: The do before talk attitude has sustained the IndieWeb community with a combination of online spaces, on the ground events, and lack of formal structure that provide lessons to CC Global Networks.</li>
      <li>Reducing Barriers and Addressing Diversity: A critical reflection of design choices, community efforts, and personal mistakes as we do the work that must be done.</li>
      <li>Augmenting Learning: The network the IndieWeb community creates involves wikis, bots, IRC, blogs, metadata, and a lot of fun. Come see how learning in the Open happens.</li>
      <li>Agency and Learning: The IndieWeb community gets driven by individual goals first, and when these goals align with the principles and values of the community the network grows. By examining how learners develop in IndieWeb we can see open examples of Community as the Curriculum </li>  
    </ul>
    <h3>
      Audience and Delivery Method
    </h3>
    <p>
      This session will appeal to creative, activists and allies in other member organizations. Through our remixes we will share examples of how the Commons gets built. We will deliver the content in a production based manner inviting the audience to blog, remix and repost as we go. We will utilizize personal websites during the session. If someone does not have a place online we will summit attendees build their own websites through the workshop. Participants will then post reflections of learning from their own websites during the workshop. 
    </p>
