     <p>Someone just asked what to the letters <a href="https://clmooc.com/">c l m o o c</a> mean so I did the #clmooc thing and responded with a string of poetry. </p>
</p>We<br>
Seek glory in no letters<br>
beyond those<br>
others master<br>
not<br>
teachers of writing<br>
more writers<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;of teaching<br>
applying our pens<br>
&nbsp;&nbsp;&nbsp;of poetry<br>
to possibilities atop<br>
the pupils' paper</p>
<p>#clmooc according to lore<br>
bore witness<br>
snippets in history<br>
when networked learning<br>
a mystery<br>
charlatan's danced<br>
with snake oil MOOCS<br>
yet few cooks,<br>
connected to NWP<br>
designed a space for me<br>
us, them<br>
bards with blogs</p>
<p>after last bell<br>
hallways emptied<br>
only sound<br>
loose locker door<br>
hanging over a<br>
smashed crayon<br>
burnt orange reminder<br>
of child's smiles<br>
connected learning MOOC<br>
spurt forth<br>
encouraging<br>
meaning to burst<br>
open like final<br>
school doors</p>
<p>Now,<br>
MOOC's shine<br>
revealed as polish<br>
of privilege<br>
#clmooc holds<br>
roots of a rudderless<br>
definition massively open,<br>
not enrolled,<br>
just networked nomads<br>
who<br>
learn out loud<br>
Open, Seeking<br>
Poets, Painters,<br>
Art = #literacies</p>
<p>#clmooc<br>
existed, persisted<br>
in siloed spaces<br>
capitalism erases<br>
like many teachers<br>
called<br>
Google+ our home<br>
until, corporate greed<br>
left us alone<br>
lost<br>
so we roam<br>
in spaces we own<br>
web rings<br>
and rss feeds<br>
silent sundays<br>
weekend haikus</p>
<p>water colors<br>
turned to poems<br>
remixed into jazz<br>
melodies<br>
converted to a zine<br>
cut up<br>
and those lucky enough<br>
find a #clmooc postcard<br>
doodle or poem<br>
inscribed<br>
for we make no<br>
distinction between<br>
physical and digital<br>
#justfriends</p>
