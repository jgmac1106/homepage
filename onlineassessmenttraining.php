<p>Goal: Participants in this session will develop strategies to adapt their assessments to online learning. We will discuss how feedback and not a measurement tool drives learning and develop ideas for continuous checks for understanding.</p>
<p>Session Length: 45 min synchronous, 1 hour asynchronous</p>
<p>Objectives:</p>
<ul>
<li>Brainstorm online assessment strategies aligned to course outcomes</li>
</ul>
<ul>
<li>Create a rubric in Blackboard</li>
<li>Create a quiz in Blackboard</li>
<li>Write feedback</li>
<li>Use polling tools for informal assessment</li>
</ul>
<p>Schedule:</p>
<ul>
<li>5 minute entrance slip</li>
<li>10</li>
<li>15 minute Begin at the End</li>
<li>15 Minute Frequent Checks for Understanding</li>
<li>10 minutes Deliver effective feedback</li>
</ul>
<p>Learning Activities:</p>
<p>Begin by discussing their overall course outcomes. You can use the 5 year experiment (Wiggins). If students met fiuve years from now and needed to use your course content what would the student who took your class know and be able to do?</p>
<p>You will not have time to complete the table but model how to take one course objective and identify the expected gains in knowledge and skills.</p>
<p>Then as a group brainstorm ideas on how evidence of this growth can be elicited in online environments. Many attendees will have no training in online teaching or basic pedagogy and assessment techniques. Have a list of assessment ideas ready.</p>
