<p>After modeling how to use the graphic organizer demo how to make a rubric in Blackboard. Share a video tutorial with participants as well.</p>
<p>After talking about final and major assessments shift to shorter more frequent checks for understanding. Explain the research that this helps to support the cognitive presence of students while also allowing you to assess and activate prior knowledge.</p>
<p>Demo how to create a quiz in blackboard. Encourage participants to check to see if their text comes with ready made question banks.</p>
<p>Then demo how polling tools can be used for quick assessments, by asking to rate their efficacy on assessing students online.</p>
<p>Finally shift the conversation to delivering feedback which improves rather than simply measures learning. Provide a rubric for a discussion board. Pass out example posts. Demonstrate how you rate students using the language of the rubric, connect to explicit evidence of their progress, and provide actionable feedback.</p>
