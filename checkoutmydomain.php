  <div class="e-content">
   <p>This Spencer Foundation Small Research Grant seeks 50,000 to support the #CheckOutMyDomain project, which will improve digital literacy and civic education by allowing patrons to check out website URLs from their local library.</p>
<h2>Problem</h2>
<p>The domination of the web by[MJG1] &nbsp;social media giants has dark implications for society and children. The rise of algorithmic news feeds, which prioritize or diminish content based on automated decisions whose logic is rarely apparent to users (Eslami et al., 2015) has contributed to the rise of fake news with over 70% of all misinformation arising on just seven websites (Knight Foundation, 2018). Youth now have friendships determined in media environments supported using business models predicated on platforms&rsquo; capacities to aggregate and commodify users&rsquo; participation (Cohen, 2013) through likes and reshares.</p>
<p>Yet more and more communities benefit from the networking possibilities provided by social media. How can we have a robust local web that supports rather undermines democracy? We believe you begin by encouraging a local web. History has taught us this requires a digitally literate society and a local library.</p>
<p>We believe establishing the local library as a place to onboard patrons to the web will help overcome issues we face in education (Leu et al., 2015), economics (Introna, 2014), and politics (Pariser, 2012). This proposal would create a system for local library patrons to check out a domain, a web address (url), and storage. Using a Deweyian lens of democratic education, we believe our libraries should be designed as anchors of the local web.</p>
<p>We believe encouraging people to have their own website allows them to[MJG2] &nbsp;own their content, control their data and to build their networks as they in turn support the local web with their growing skills. If funded this grant will allow participants to build digital literacy skills by contributing to public knowledge, preserving culture, and connecting with each other.</p>
<h2>Theoretical Perspective</h2>
<p>As educators we must shape the spaces of learning to meet ideals of democratic education (Dewey 1934). For Dewey democracy is a way of being, of experiencing the best collective action humans could muster. The web is no different. Building a better web builds a better democracy.</p>
<p>Dewey (1927) noted, &lsquo;[a] Great Community can only occur with free and full intercommunication&rsquo; (p. 211). In terms of fighting fake news and wrestling back control of from&nbsp;powerful search engine and social media companies, we must build a shared experience around common goals with elements of experimentation and criticality (Bruce &amp; Bishop, 2005). Such community inquiry allows people to construct knowledge from both the personal and the collective (Shore et al., 1996).</p>
<p>Accordingly based on Dewey (1934) that thinking occurs in "forked road situations" where we are presented with problems and must work through proposed alternatives. Dewey argued that through training we can transform learners&rsquo; natural capacities to project future outcomes into the habits of critical inquiry.</p>
<h2>Research Plan</h2>
<p>This grant will utilize formative experiments (Cobb, Confrey, diSessa, Lehrer, &amp; Schauble, 2003; Reinking &amp; Bradley, 2008) to ask what could be from a mixed-methods perspective. This project will use four to meet our goals: 1:) Collect Baseline Data, 2:) Develop Intervention. 3:) Implement Intervention, and 4:) Retrospective Analysis.</p>
<p>We begin our study with the pedagogical goals of improving the digital literacy skills of the participants while also supporting the quality of the local web.</p>
<p>Specifically:</p>
<p>&middot; Does the ability to check out a domain from a library lead to an increase in local content and civic engagement on the web?</p>
<p>&middot; Do library patrons who check out a domain show improvement in digital literacy skills?</p>
<h3>Recruitment Plan</h3>
<p>In year one we will work with libraries to recruit participants. In year one we will have 30 participants at two libraries for a total of 60 patrons. Participants will be recruited from existing tech programs and participants who attend adult literacy programs. This approach should hopefully lead to pool of candidates that is multi-generational and have a variety of technical abilities.</p>
<p>In year two the recruitment plan will rely heavily on year one participants to encourage people in their existing local networks to join. We will allow up to 100 participants at each library and rely on year one participants to serve as leads.</p>
<h3>Phase 1: Baseline Data</h3>
<p>The project will begin with a brief snapshot of the digital landscape of communities and partnerships. Baseline data for participants will be conducted in one to one interviews about how they use the web. Semi structured interview questions will ask about past social media experience, computer experience, website building, social media, and HTML ability.</p>
<p>Social media will be examined to see if there are popular local hashtags. All data from social media will only be reported in the aggregate. Only artifacts, like posts to social media, will be included for participants with explicit permission.</p>
<h4>Phase 1: Data Analysis</h4>
<p>In order to understand the local context in which we will iterate on our design, the quantitative and qualitative data collected prior to the study will be triangulated using thematic network analysis (Attride-Sterling, 2001).</p>
<p>We will then use persona design (Klein, 2015) as a method of case study analysis. In persona design you create fictional representations of users. By empathizing with user needs we will be able to customize our interventions for our participants.</p>
<h4>Phase 1: Expected Outcomes</h4>
<p>By the end of phase 1 we will have an understanding of the users and local communities andwill know how to adapt our pre-existing training programs/.</p>
<h3>Phase 2: Develop Interventions</h3>
<p>In year one and two, phase 2 will begin with iterating on training sessions. Dr. McVerry and the graduate student will use the persona and community data to plan the two day institutes. We will draw on a decade of experience in developing production-based learning camps (Celik, 2008). The principal investigator has organized many conferences and events using unconference pedagogies found in edcamps and barcamps.</p>
<p>The institutes will involve instruction in the morning for group sessions and then participant lead sessions in the afternoon. The second day will be a design studio. The day will conclude with demos of websites or tools built during the meeting.</p>
<p>The first institute will cover what can be done from your own domain, our responsibilities to each other online, how to build a basic blog and website. The second session will focus on building your most credible self and will encourage participants to document something they learn or an activity to make the world a better place.</p>
<h4>Phase 2 Data Analysis</h4>
<p>Data analysis on phase two will focus on feasibility and ecological validity[MJG3] . Field notes and reflections from the designers will be analyze to our pedagogical goal in terms of audience needs that emerged from phase one. We will differentiate instructional routines accordingly.</p>
<h4>Phase 2: Expected Outcomes</h4>
<p>A plan and developed sessions for face to face and online meetings.</p>
<h3>Phase 3: Intervention Implementation</h3>
<p>In years one and two the implementation of interventions will follow roughly the same schedule developed in phase two (See the timeline in Appendix). Participants will attend two face to face sessions and be required to complete online training to keep their domain.</p>
<h4>Phase 3 Data Analysis</h4>
<p>To measure the progress of our pedagogical goals, both qualitative and quantitative data will be collected throughout the study. We will use two measures for digital literacy skills.</p>
<p>Participant HTML ability will be used as a proxy measure for overall digital literacies skills. Blog post and/or website pages will be collected as&nbsp;set intervals. HTML parsers, which can separate pieces of websites based on specific tags, will be used to quantify the use of HMTL and to track changes to websites over time.</p>
<p>We will also develop a reflection based badging system[MJG4] &nbsp;to track digital literacies skills. A series of badges (see Appendix for example) will be developed from the NCTE Framework for 21st Century Curriculum and Assessment (2017). Participants will submit reflections explaining how different media they create meets the criteria for competency.</p>
<p>To track network effect, a variety of tools will be used. First a feed of all user websites and comments will be collected. Second, local hashtags will be monitored for frequency of growth. Finally, webmentions.io will be used to capture whenever a website receives a mention across the web. A webmention is a link between two websites where website A lets website B know, "I linked to your site," and then website A can display that mention. To analyze this data dimensions of tie strength, how well people and community are connected, will be measured in on three scales trust, enjoying socializing, or discussing important matters (Marin &amp; Hampton,2007) across a media (Jaimeson, Bose, &amp;. Kobyashi, 2018).</p>
<p>In terms of qualitative data, we will also take extensive field notes and write weekly reflections as participant observers in the study. We will also conduct content analysis of users&rsquo; post to identify explicit mention of their own improving digital literacy skills or the overall health of the local web.</p>
<h4>Phase 3: Expected Outcomes</h4>
<p>After phase three in year two up to 60 participants will be trained. If they attend three of the online sessions and the second training participants can check out their domain for a second year.</p>
<p>After phase three in year two, additional participants will complete the training. This will allow us to measure the impact of the network and the digital literacy skills of participants at numerous timepoints.</p>
<h3>Phase Four: Retrospective Analysis</h3>
<p>Tashakkori and Teddlie (1998) called for &ldquo;theoretical triangulation&rdquo; (p. 41), which will align with experiencing democracy education for the web. We will be conducting an analysis of all the data to push our theoretical understandings forward, so we can make recommendations for making the public library the keystone of the local web.</p>
<p>By employing &ldquo;a variant of Glaser and Strauss&rsquo;s (1967) &ldquo;constant comparative method&rdquo; (p. 38) we will identify key moments that illustrate how the intervention either enhanced or inhibited the pedagogical goal.</p>
<p>The data collected by the badging platform will be invaluable. We wil have descriptive statistics to examine for patterns. We can also perform content analysis on user artifacts submitted in badge applications.</p>
<p>&nbsp;Given the smaller sample size we may only be able to report descriptive or difference in means testing of HTML skills. However, if we meet the power assumptions, We will compare growth of digital literacy skills, by building growth curve models.</p>
<p>We will also utilize social network analysis to understand the infrastructures, human and nonhuman relationships, affordances (Introna, 2014) through our continued measure of tie strength along trust, enjoying socializing, or discussing important matters</p>
<h2>Conclusion</h2>
<p>#CheckoutMyDomain Project seeks to build social problem-based learning around shared goals. In conjunction with local libraries we provide users a place to live online and help build this infrastructure.</p>
