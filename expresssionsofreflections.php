When Piper glances<br/>
Bard<br/>
reflections of<br/>
expressions in<br/>
contrary states<br/>
bounce back<br/>
from Blake's<br/>
plates.<br/>
<br/>
Constraints<br/>
on creativity<br/>
in a marriage of fates.<br/>
If it is God that is<br/>
within us<br/>
then it must be Satan who<br/>
dims us<br/>
